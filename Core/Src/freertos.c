/**
  * @brief  Function implementing the defaultTask thread.
  * @param  argument: Not used
  * @retval None
  */
/* USER CODE END Header_StartDefaultTask */
void StartDefaultTask(void const * argument)
{
  /* USER CODE BEGIN StartDefaultTask */
	char read_buff[20] = {0};
	char write_buff[20] = {0};
	bool is_successful = false;

	FRESULT res;
	if((res = f_mount(&SDFatFS, SDPath, 1)) == FR_OK)
	{
		if(f_open(&SDFile, "test.txt", FA_WRITE | FA_OPEN_ALWAYS) == FR_OK)
		{
			UINT bw;
			sprintf(write_buff,"Hello World!!!!");
			f_write(&SDFile, write_buff, sizeof(write_buff), &bw);
			f_close(&SDFile);
		}

		if(f_open(&SDFile, "test.txt", FA_READ | FA_OPEN_ALWAYS) == FR_OK)
		{
			UINT br;
			f_read(&SDFile, read_buff, sizeof(read_buff), &br);
			f_close(&SDFile);
		}

		if(strcmp(write_buff,read_buff) == 0)
		{
			//Read and write buffers are equal so SD card with FATFS and freeRTOS works fine
			is_successful = true;
		}
	}
  /* Infinite loop */
  for(;;)
  {
    osDelay(1);
  }
  /* USER CODE END StartDefaultTask */
}

